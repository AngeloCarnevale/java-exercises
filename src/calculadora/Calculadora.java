package calculadora;

public class Calculadora {

    public Double soma (Double valorinicial, Double valorfinal) {
        return valorinicial + valorfinal;
    }
    public Double subtracao(Double valorinicial, Double valorfinal) {
        return valorinicial - valorfinal;
    }
    public Double multiplicacao (Double valorinicial, Double valorfinal) {
        return valorinicial * valorfinal;
    }
    public Double divisao (Double valorinicial, Double valorfinal) {
        return valorinicial / valorfinal;
    }
}
